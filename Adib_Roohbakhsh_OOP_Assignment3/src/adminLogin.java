    public void fillTable() {
        String[] header = new String[]{"Plan ID", "username", "Model", "OS Type", "Memory Size", "handset price", "Internet Quota", "Cap Limit", "Expiry Date", "City", "ABN", "Number of Employees", "Monthly Payment"};
        DefaultTableModel model = new DefaultTableModel(header, 0);
        this.jTable2.setModel(model);
         String text = (String)userCombo1.getSelectedItem();
       String [] fields = text.split(":");
       int id = Integer.parseInt(fields[1]);
       User user=mobileCompany.getUsers().get(id);
       ArrayList<MobilePlan> plans=new ArrayList<>();
       for (MobilePlan plan : user.getPlans().values()) {
       plans.add(plan);
       }
       if(!this.filterModelText.getText().isEmpty()){
           plans=MobilePlan.filterByMobileModel(plans, this.filterModelText.getText());
       }
       
       if(!this.filterDateText.getText().isEmpty()){
            fields = this.filterDateText.getText().split("/");
                int year = Integer.parseInt(fields[0]);
                int month = Integer.parseInt(fields[1]);
                int day = Integer.parseInt(fields[2]);
                MyDate date = new MyDate(year, month, day);
                plans = MobilePlan.filterByExpiryDate(plans, date);
       }
       
        for (MobilePlan plan : plans) {
       
            String[] row = new String[13];
            row[0] = plan.getId() + "";
            row[1] = plan.userName;
            row[2] = plan.getHandsetModel();
            row[3] = plan.handset.getType().toString();
            row[4] = plan.handset.getMemorySize() + "";
            row[5] = plan.handset.getPrice() + "";
            row[6] = plan.internetQuota + "";
            row[7] = plan.capLimit + "";
            row[8] = plan.getExpiryDate().toString();
            if (plan instanceof PersonalPlan) {
                row[9] = ((PersonalPlan) plan).city;
                row[10] = "NA";
                row[11] = "NA";
            } else {
                row[9] = "NA";
                row[10] = ((BusinessPlan) plan).ABN + "";
                row[11] = ((BusinessPlan) plan).numberOfEmployees + "";
            }

            row[12] =  plan.calcPayment(10)+"";
            model.addRow(row);
        }
    }
          public void fillTable1() {
        String[] header = new String[]{"UserName", "MonthlyPayment"};
        DefaultTableModel model = new DefaultTableModel(header, 0);
        this.paymentTable.setModel(model);
        ArrayList<User> users=new ArrayList<>();
        for(User user: mobileCompany.getUsers().values()){
            users.add(user);
        }
       if(!this.sortUserName.isSelected())
           Collections.sort(users);
       if(this.sortPayment.isSelected())
           users.sort(Comparator.comparingDouble(x -> x.calcTotalPayments(mobileCompany.getFlatRate())));

           for (User user:users) {
            String[] row = new String[2];
            row[0] = user.getName();
            row[1] =  user.calcTotalPayments(10)+"";
            model.addRow(row);
        }
    }
             public void fillTable2() {
        String[] header = new String[]{"City Name", "Monthly Payment"};
        DefaultTableModel model = new DefaultTableModel(header, 0);
        this.repotTable.setModel(model);
        HashMap<String,Double> cityPayment = mobileCompany.getTotalPaymentPerCity();
        for(String city :cityPayment.keySet()){
            String[] row=new String[2];
            row[0]=city;
            row[1] = cityPayment.get(city) + "";
            model.addRow(row);
        }  
    }
     public void fillTable3() {
         String[] header = new String[]{"Mobile Model", "Monthly Payment","Average Payment"};
        DefaultTableModel model = new DefaultTableModel(header, 0);
        this.jTable1.setModel(model);
        HashMap<String, Double> hashMap1 = mobileCompany.getTotalPaymentPerMobileModel(10,mobileCompany.getAdminUsername(),mobileCompany.getAdminPassword());
        HashMap<String, Integer> hashMap2 = mobileCompany.getTotalCountPerMobileModel(mobileCompany.getAdminUsername(),mobileCompany.getAdminPassword());
        for (HashMap.Entry<String, Double> entry : hashMap1.entrySet()) {
            String[] row=new String[3];
            row[0]=entry.getKey();
            row[1]=entry.getValue()+"";
            row[2]= entry.getValue() / hashMap2.get(entry.getKey())+"";
            model.addRow(row);
        }
     }
