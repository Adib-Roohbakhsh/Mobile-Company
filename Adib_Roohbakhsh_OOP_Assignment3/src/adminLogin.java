    private void addAddPlanbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addAddPlanbuttonActionPerformed
        String ID = (String) userComboAddPlan.getSelectedItem();
        try {
            int id = Integer.parseInt(this.planIdTF.getText());
            String username = this.userNameTF.getText();
            String model = this.modelTF.getText();
            MobileType type = MobileType.valueOf(this.osCombobox.getSelectedItem().toString());
            int size = Integer.parseInt(this.memorySizeTF.getText());
            double price = Double.parseDouble(this.priceTF.getText());
            int quota = Integer.parseInt(this.internetQuotaTF.getText());
            int cap = Integer.parseInt(this.capLimitTF.getText());
            int day = Integer.parseInt(this.dayTF.getText());
            int month = Integer.parseInt(this.monthTF.getText());
            int year = Integer.parseInt(this.yearTF.getText());
            MyDate date = new MyDate(year, month, day);
            MobilePhone phone = new MobilePhone(model, type, size, price);
            MobilePlan plan = null;
            if (this.pPlanRadioButton.isSelected()) {
                String city = null;
                try {
                    city = this.abnOrCityTF.getText();
                    plan = new PersonalPlan(username, id, phone, quota, cap, city, date);
                } catch (PlanExceptionUserName peun) {
                    try {
                        username = peun.getNewUserName();
                        JOptionPane.showMessageDialog(this, peun);
                        plan = new PersonalPlan(username, id, phone, quota, cap, city, date);
                    }catch (PlanException pe){
                        id = pe.getNewID();
                        JOptionPane.showMessageDialog(this, pe);
                        plan = new PersonalPlan(username, id, phone, quota, cap, city, date);
                    }

                } catch (PlanException pe) {
                    id = pe.getNewID();
                    JOptionPane.showMessageDialog(this, pe);
                    plan = new PersonalPlan(username, id, phone, quota, cap, city, date);
                }
            } else {
                int noEmp = 0;
                int abn = 0;
                try {
                    abn = Integer.parseInt(this.abnOrCityTF.getText());
                    noEmp = Integer.parseInt(this.nOfEmploeesTF.getText());
                    plan = new BusinessPlan(username, id, phone, quota, cap, noEmp, abn, date);
                } catch (PlanExceptionUserName peun) {
                    try {
                        username = peun.getNewUserName();
                        JOptionPane.showMessageDialog(this, peun);
                        plan = new BusinessPlan(username, id, phone, quota, cap, noEmp, abn, date);
                    }catch (PlanException pe){
                        id = pe.getNewID();
                        JOptionPane.showMessageDialog(this, pe);
                        plan = new BusinessPlan(username, id, phone, quota, cap, noEmp, abn, date);
                    }
                } catch (PlanException pe) {
                    id = pe.getNewID();
                    JOptionPane.showMessageDialog(this, pe);
                    plan = new BusinessPlan(username, id, phone, quota, cap, noEmp, abn, date);
                }
            }

            if (mobileCompany.addPlan(Integer.parseInt(ID.split(":")[1]),plan,mobileCompany.getAdminUsername(),mobileCompany.getAdminPassword())) {
                JOptionPane.showMessageDialog(this, "Plan added successfully :)");
                this.planIdTF.setText("");
                this.userNameTF.setText("");
                this.modelTF.setText("");
                this.memorySizeTF.setText("");
                this.priceTF.setText("");
                this.internetQuotaTF.setText("");
                this.capLimitTF.setText("");
                this.dayTF.setText("");
                this.modelTF.setText("");
                this.yearTF.setText("");
                this.abnOrCityTF.setText("");
                this.nOfEmploeesTF.setText("");
            } else {
                JOptionPane.showMessageDialog(this, "Plan adding failed");
            }
        } catch (NumberFormatException | PlanException | PlanExceptionUserName e) {
            JOptionPane.showMessageDialog(this, e);
        }    }//GEN-LAST:event_addAddPlanbuttonActionPerformed

